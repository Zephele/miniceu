#include "../include/minishell.h"

int	ft_isalnum_underline(int c)
{
	if ((c >= 48 && c <= 57) || ((c >= 65 && c <= 90)
			|| (c >= 97 && c <= 122)) || (c == '_'))
	{
		return (1);
	}
	return (0);
}
